package z4;

public class Main {
    public static void main (String args[]){
        for (int i=0;i<3;i++){
            System.out.println("Hello World!");
        }

        int i=0;

        while (i<3){
            System.out.println("Hello World!");
            i++;
        }
        i=0;
        do{
            System.out.println("Hello World!");
            i++;
        } while (i<4);
    }
}
